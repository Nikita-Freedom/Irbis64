package com.example.myapplication;
/**
 *
 * @param <U>
 * @param <V>
 */
public class Pair<U, V> {
    /** */
    public U u;

    /** */
    public V v;

    /**
     *
     * @param u
     * @param v
     */
    public Pair(U u, V v) {
        this.u = u;
        this.v = v;
    }
} //  class Pair
